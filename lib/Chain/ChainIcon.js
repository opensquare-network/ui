function ChainIcon({
  chainName,
  size = 24,
  className = "",
  showTooltip = false,
}) {
  chainName = capitalize(chainName);

  const Icon = icons[chainName] || icons.DEFAULT;

  return (
    <Tooltip content={showTooltip && (chainName || "")} className={className}>
      <div>
        <img
          src={Icon.light}
          width={size}
          height={size}
          className="dark:hidden"
        />
        <img
          src={Icon.dark}
          width={size}
          height={size}
          className="hidden dark:block"
        />
      </div>
    </Tooltip>
  );
}

